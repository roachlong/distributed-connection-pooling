DROP TABLE IF EXISTS outbox_payload;
DROP TABLE IF EXISTS outbox;

-- Hot queue table (must remain single-family for SKIP LOCKED)
CREATE TABLE outbox (
  id UUID NOT NULL DEFAULT gen_random_uuid(),

  aggregatetype STRING NOT NULL,
  aggregateid   STRING NOT NULL,
  type          STRING NOT NULL,
  "timestamp"   TIMESTAMP NOT NULL DEFAULT now(),

  is_published      BOOL NOT NULL DEFAULT false,
  publish_timestamp TIMESTAMP NULL,

  CONSTRAINT outbox_pkey PRIMARY KEY (id)
) LOCALITY REGIONAL BY ROW;

-- Cold payload table keyed by id; also regional by row for co-location
CREATE TABLE outbox_payload (
  id UUID NOT NULL,
  payload BYTES NULL,

  CONSTRAINT outbox_payload_pkey PRIMARY KEY (id),
  CONSTRAINT outbox_payload_fk
    FOREIGN KEY (id) REFERENCES outbox(id) ON DELETE CASCADE
) LOCALITY REGIONAL BY ROW;

-- Dispatcher index (region first improves locality filtering)
-- Partial index kept small: unpublished only
CREATE INDEX idx_outbox_unpublished_by_region_time
ON outbox ("timestamp", id)
STORING (publish_timestamp)
WHERE is_published = false;

